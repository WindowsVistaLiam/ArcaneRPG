const RARITY_INFO = {
  common: {
    label: "Commun",
    value: 10,
  },
  rare: {
    label: "Rare",
    value: 40,
  },
  epic: {
    label: "Épique",
    value: 120,
  },
  legendary: {
    label: "Légendaire",
    value: 350,
  },
  mythic: {
    label: "Mythique",
    value: 1000,
  },
}

function createCard({
  characterKey,
  characterName,
  variant,
  rarity,
  description,
  image = "",
  faction = "Inconnue",
  season = "Arcane",
  tags = [],
}) {
  const rarityInfo = RARITY_INFO[rarity]

  if (!rarityInfo) {
    throw new Error(`Rareté invalide pour ${characterName}: ${rarity}`)
  }

  return {
    key: `${characterKey}_${rarity}_${variant
      .toLowerCase()
      .normalize("NFD")
      .replace(/[̀-ͯ]/g, "")
      .replace(/[^a-z0-9]+/g, "_")
      .replace(/^_|_$/g, "")}`,

    characterKey,
    characterName,
    name: `${characterName} — ${variant}`,

    rarity,
    rarityLabel: rarityInfo.label,
    value: rarityInfo.value,

    image,
    description,
    faction,
    season,
    tags,
  }
}

